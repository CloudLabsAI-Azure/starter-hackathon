    async def _get_file_content(self, file_path: str, branch: str) -> Optional[Dict[str, Any]]:
        """
        Fetch file content from GitHub repository.
        
        Args:
            file_path: Path to file in repository
            branch: Branch name
            
        Returns:
            Dict with 'content' (decoded string) and 'sha' (for updates)
        """
        try:
            url = f"{self.github_api_base}/repos/{self.github_repo}/contents/{file_path}"
            params = {"ref": branch}
            
            async with httpx.AsyncClient(timeout=30.0) as client:
                response = await client.get(url, headers=self._get_github_headers(), params=params)
                response.raise_for_status()
                
                file_data = response.json()
                
                # Decode base64 content
                content_b64 = file_data.get("content", "")
                content = base64.b64decode(content_b64).decode("utf-8")
                
                logger.info(f"Fetched file content: {file_path} ({len(content)} chars)")
                
                return {
                    "content": content,
                    "sha": file_data.get("sha")
                }
                
        except httpx.HTTPStatusError as e:
            logger.error(f"GitHub API error fetching file {file_path}: {e.response.status_code}")
            return None
        except Exception as e:
            logger.error(f"Error fetching file {file_path}: {str(e)}")
            return None
    
    async def _create_branch(self, branch_name: str, base_branch: str) -> bool:
        """
        Create a new branch from base branch.
        
        Args:
            branch_name: Name for new branch
            base_branch: Base branch to create from
            
        Returns:
            True if successful
        """
        try:
            # Get SHA of base branch
            ref_url = f"{self.github_api_base}/repos/{self.github_repo}/git/ref/heads/{base_branch}"
            
            async with httpx.AsyncClient(timeout=30.0) as client:
                response = await client.get(ref_url, headers=self._get_github_headers())
                response.raise_for_status()
                
                base_sha = response.json()["object"]["sha"]
                
                # Create new branch
                create_url = f"{self.github_api_base}/repos/{self.github_repo}/git/refs"
                create_data = {
                    "ref": f"refs/heads/{branch_name}",
                    "sha": base_sha
                }
                
                response = await client.post(
                    create_url,
                    headers=self._get_github_headers(),
                    json=create_data
                )
                response.raise_for_status()
                
                logger.info(f"Created branch: {branch_name} from {base_branch}")
                return True
                
        except httpx.HTTPStatusError as e:
            if e.response.status_code == 422:
                logger.warning(f"Branch {branch_name} already exists")
                return True  # Branch exists, proceed
            logger.error(f"GitHub API error creating branch: {e.response.status_code}")
            return False
        except Exception as e:
            logger.error(f"Error creating branch {branch_name}: {str(e)}")
            return False
    
    async def _update_file(
        self,
        file_path: str,
        content: str,
        message: str,
        branch: str,
        sha: str
    ) -> bool:
        """
        Update file content on a branch.
        
        Args:
            file_path: Path to file
            content: New file content
            message: Commit message
            branch: Branch name
            sha: Current file SHA (for update)
            
        Returns:
            True if successful
        """
        try:
            url = f"{self.github_api_base}/repos/{self.github_repo}/contents/{file_path}"
            
            # Encode content to base64
            content_b64 = base64.b64encode(content.encode("utf-8")).decode("utf-8")
            
            data = {
                "message": message,
                "content": content_b64,
                "branch": branch,
                "sha": sha
            }
            
            async with httpx.AsyncClient(timeout=30.0) as client:
                response = await client.put(
                    url,
                    headers=self._get_github_headers(),
                    json=data
                )
                response.raise_for_status()
                
                logger.info(f"Updated file {file_path} on branch {branch}")
                return True
                
        except httpx.HTTPStatusError as e:
            logger.error(f"GitHub API error updating file: {e.response.status_code} - {e.response.text}")
            return False
        except Exception as e:
            logger.error(f"Error updating file {file_path}: {str(e)}")
            return False
    
    async def _create_pull_request(
        self,
        title: str,
        body: str,
        head: str,
        base: str
    ) -> Optional[Dict[str, Any]]:
        """
        Create a pull request.
        
        Args:
            title: PR title
            body: PR description
            head: Source branch
            base: Target branch
            
        Returns:
            PR data with number and URL
        """
        try:
            url = f"{self.github_api_base}/repos/{self.github_repo}/pulls"
            
            data = {
                "title": title,
                "body": body,
                "head": head,
                "base": base
            }
            
            async with httpx.AsyncClient(timeout=30.0) as client:
                response = await client.post(
                    url,
                    headers=self._get_github_headers(),
                    json=data
                )
                response.raise_for_status()
                
                pr = response.json()
                logger.info(f"Created PR #{pr['number']}: {pr['html_url']}")
                
                return {
                    "number": pr["number"],
                    "html_url": pr["html_url"],
                    "url": pr["url"]
                }
                
        except httpx.HTTPStatusError as e:
            logger.error(f"GitHub API error creating PR: {e.response.status_code} - {e.response.text}")
            return None
        except Exception as e:
            logger.error(f"Error creating pull request: {str(e)}")
            return None
