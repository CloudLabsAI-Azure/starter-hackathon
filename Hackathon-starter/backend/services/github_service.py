    def _parse_labels_from_analysis(self, analysis: str) -> List[str]:
        """Parse suggested labels from AI analysis text"""
        labels = []
        
        # Look for common label patterns in analysis
        analysis_lower = analysis.lower()
        
        # Issue type labels
        if "bug" in analysis_lower:
            labels.append("bug")
        if "feature" in analysis_lower or "enhancement" in analysis_lower:
            labels.append("enhancement")
        if "documentation" in analysis_lower or "docs" in analysis_lower:
            labels.append("documentation")
        if "question" in analysis_lower:
            labels.append("question")
        
        # Priority labels
        if "critical" in analysis_lower or "urgent" in analysis_lower:
            labels.append("priority: critical")
        elif "high priority" in analysis_lower:
            labels.append("priority: high")
        elif "low priority" in analysis_lower:
            labels.append("priority: low")
        
        # Security labels
        if "security" in analysis_lower:
            labels.append("security")
        
        # Remove duplicates
        return list(set(labels))
    
