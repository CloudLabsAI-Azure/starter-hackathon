"""
GitHub Service - Integration with GitHub API for automated issue and PR analysis

Provides functionality for:
- Issue analysis and auto-labeling
- Pull request review with AI insights
- Auto-fix PR creation with AI-generated code
- Integration with MCP server for AI analysis
"""

import os
import httpx
import logging
import re
import base64
from typing import Dict, Any, Optional, List
from datetime import datetime

logger = logging.getLogger(__name__)


class GitHubService:
    """Service for GitHub API integration with AI-powered analysis"""
    
    def __init__(self):
        """Initialize GitHub service with configuration from environment"""
        self.github_token = os.getenv("GITHUB_TOKEN")
        self.github_repo = os.getenv("GITHUB_REPO")  # Format: "owner/repo"
        self.mcp_server_url = os.getenv("MCP_SERVER_URL", "http://localhost:8000")
        
        if not self.github_token:
            logger.warning("GITHUB_TOKEN not set - GitHub integration will be disabled")
        
        if not self.github_repo:
            logger.warning("GITHUB_REPO not set - GitHub integration will be disabled")
        
        self.github_api_base = "https://api.github.com"
        self.github_api_version = "2022-11-28"
    
    def _get_github_headers(self) -> Dict[str, str]:
        """Get headers for GitHub API requests"""
        return {
            "Authorization": f"Bearer {self.github_token}",
            "Accept": "application/vnd.github+json",
            "X-GitHub-Api-Version": self.github_api_version
        }
    
    # ========================================
    # Main Workflow Methods
    # ========================================
    
    async def analyze_and_label_issue(self, issue_number: int) -> Dict[str, Any]:
        """
        Main workflow: Analyze issue with AI and auto-label.
        
        Steps:
        1. Fetch issue from GitHub API
        2. Call MCP /analyze endpoint for AI analysis
        3. Parse analysis for issue type, priority, suggested labels
        4. Add labels to the issue via GitHub API
        5. Post AI analysis as comment on the issue
        
        Args:
            issue_number: The issue number to analyze
            
        Returns:
            Dict with success status, labels added, and comment URL
        """
        try:
            logger.info(f"Starting analysis for issue #{issue_number}")
            
            # Step 1: Fetch issue details
            issue = await self._get_issue(issue_number)
            if not issue:
                return {
                    "success": False,
                    "error": f"Issue #{issue_number} not found"
                }
            
            # Step 2: Analyze issue with AI
            analysis = await self._analyze_issue_with_ai(
                title=issue["title"],
                body=issue.get("body", "")
            )
            
            if not analysis:
                return {
                    "success": False,
                    "error": "AI analysis failed"
                }
            
            # Step 3: Parse analysis for labels
            suggested_labels = self._parse_labels_from_analysis(analysis)
            
            # Step 4: Add labels to issue
            if suggested_labels:
                await self._add_labels(issue_number, suggested_labels)
                logger.info(f"Added labels to issue #{issue_number}: {suggested_labels}")
            
            # Step 5: Post AI analysis as comment
            comment_body = f"""## 🤖 AI Analysis

{analysis}

---
*Generated by Zava AI Portal using Azure OpenAI*
"""
            comment_url = await self._create_comment(issue_number, comment_body)
            
            logger.info(f"Successfully analyzed issue #{issue_number}")
            return {
                "success": True,
                "issue_number": issue_number,
                "labels_added": suggested_labels,
                "comment_url": comment_url,
                "analysis": analysis
            }
            
        except Exception as e:
            logger.error(f"Error analyzing issue #{issue_number}: {str(e)}")
            return {
                "success": False,
                "error": str(e)
            }
    
    async def review_pull_request(self, pr_number: int) -> Dict[str, Any]:
        """
        Main workflow: Review PR with AI insights.
        
        Steps:
        1. Fetch PR details and changed files from GitHub API
        2. Call MCP /analyze endpoint for code review
        3. Parse review for security, quality, performance issues
        4. Post review comment on PR
        
        Args:
            pr_number: The PR number to review
            
        Returns:
            Dict with success status, review summary, and comment URL
        """
